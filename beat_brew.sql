  Date DATE
);

--DROP TABLE IF EXISTS Association; 
CREATE TABLE IF NOT EXISTS Association (
  Association_ID SERIAL PRIMARY KEY,
  Nom VARCHAR(100),
  Site_web VARCHAR(200),
  UNIQUE (Nom, Site_web)
);

--DROP TABLE IF EXISTS Artistes; 
CREATE TABLE IF NOT EXISTS Artistes (
  Artistes_ID SERIAL PRIMARY KEY,
  Nom VARCHAR(100),
  Genre VARCHAR(100), 
  Nb_Membres INTEGER, 
  UNIQUE (Nom, genre, Nb_Membres)
);

--DROP TABLE IF EXISTS Personne; 
CREATE TABLE IF NOT EXISTS Personne (
  Personne_ID SERIAL PRIMARY KEY,
  Age INTEGER NOT NULL,
  Genre CHAR(1),
  Nationnalite VARCHAR(100)
);

--DROP TABLE IF EXISTS Playlist;
CREATE TABLE IF NOT EXISTS Playlist (
  Playlist_ID SERIAL PRIMARY KEY,
  Nom VARCHAR(100),
  IsGroupe BOOLEAN NOT NULL ,
  Nb_morceau INTEGER CHECK (Nb_morceau >= 0 AND Nb_morceau <= 20) NOT NULL,
  Artistes_ID INTEGER REFERENCES Artistes(Artistes_ID) 
);

--DROP TABLE IF EXISTS Morceau;
CREATE TABLE IF NOT EXISTS Morceau (
  Morceau_Id SERIAL PRIMARY KEY,
  Titre VARCHAR(100) NOT NULL,
  Nom_album VARCHAR(100) NULL,
  Genre VARCHAR(50)
);

--DROP TABLE IF EXISTS PlaylistMorceau;
CREATE TABLE IF NOT EXISTS PlaylistMorceau (
  Playlist_Id INTEGER REFERENCES Playlist(Playlist_Id),
  Morceau_Id INTEGER REFERENCES Morceau(Morceau_Id),
  PRIMARY KEY (Playlist_Id, Morceau_Id)
);

--DROP TABLE IF EXISTS Avis;
CREATE TABLE IF NOT EXISTS Avis (
  Avis_ID SERIAL PRIMARY KEY,
  Note INTEGER CHECK (Note >= 0 AND Note <= 10) NOT NULL,
  Commentaire TEXT, 
  Pseudo VARCHAR(200), 
  Date DATE NOT NULL,
  Tags VARCHAR(100) NULL
);

--DROP TABLE IF EXISTS Lieu; 
CREATE TABLE IF NOT EXISTS Lieu (
  Lieu_ID SERIAL PRIMARY KEY,
  Nom VARCHAR(100) NOT NULL,
  Adresse TEXT UNIQUE NOT NULL,
  Avis_ID INTEGER REFERENCES Avis(Avis_ID),
  UNIQUE (Nom, Adresse)
);

--DROP TABLE IF EXISTS Concert;
CREATE TABLE IF NOT EXISTS Concert (
  Concert_Id SERIAL PRIMARY KEY,
  Nom VARCHAR(60),
  Date DATE,
  Organisateurs VARCHAR(100),
  "Line-up" VARCHAR(255),
  Nombre_places INTEGER,
  Cause_soutien BOOLEAN,
  Espace_exterieur BOOLEAN, 
  Enfants_permis BOOLEAN,
  Prix DECIMAL(10, 2),
  Nb_volontaires INTEGER,
  ID_Lieu INTEGER REFERENCES Lieu(Lieu_ID)
);

--DROP TABLE IF EXISTS Archive; 
-- Créer une fonction pour vérifier la contrainte 
CREATE OR REPLACE FUNCTION check_archive_date()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.Date_archivage < (SELECT "Date" FROM Concert WHERE Concert_Id = NEW.Concert_Id) THEN
    RAISE EXCEPTION 'Date_archivage must be greater than or equal to Concert."Date"';
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Créer un déclencheur avant l'insertion dans CArchive
CREATE TRIGGER check_archive_date_trigger
BEFORE INSERT ON Archive
FOR EACH ROW EXECUTE FUNCTION check_archive_date();

