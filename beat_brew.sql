-- PARTIE 1) Création des tables 

--DROP TABLE IF EXISTS Historique;
CREATE TABLE IF NOT EXISTS Historique (
    Historique_ID SERIAL PRIMARY KEY,
    Type VARCHAR(100) NOT NULL CHECK (Type IN ('Connexion', 'Déconnexion', 'Modification de Profil', 'Création', 'Lecture', 'Mise à Jour', 'Suppression', 'Ajout Ami', 'Suppression Ami', 'Abonnement', 'Désabonnement', 'Publication', 'Modification de Publication', 'Suppression de Publication', 'Commentaire', 'Like', 'Partage', 'Création de Groupe', 'Rejoindre un Groupe', 'Quitter un Groupe', 'Envoi de Message Privé', 'Réception de Message Privé', 'Lecture de Message Privé', 'Création de Playlist', 'Modification de Playlist', 'Suppression de Playlist')),
    Date DATE NOT NULL
);

--DROP TABLE IF EXISTS Publication;
CREATE TABLE IF NOT EXISTS Publication (
  Publication_ID SERIAL PRIMARY KEY,
  Date DATE NOT NULL
);

--DROP TABLE IF EXISTS Message_Prive;
CREATE TABLE IF NOT EXISTS Message_Prive (
    Message_ID SERIAL PRIMARY KEY,
    Destinataire_pseudo VARCHAR(50),
    Contenu TEXT,
    Date TIMESTAMP,
    Etat VARCHAR(10) CHECK (Etat IN ('distribue', 'lu'))
);

--DROP TABLE IF EXISTS Suggestion; 
CREATE TABLE IF NOT EXISTS Suggestion (
  Suggestion_ID SERIAL PRIMARY KEY,
  Type VARCHAR(200) CHECK (Type IN (
    'Suggestion Ami',
    'Suggestion de Contenu',
    'Suggestion de Groupe',
    'Suggestion de Playlist',
    'Suggestion musicale',
    'Suggestion amis en fonction des intérêts communs',
    'Suggestion de groupes en fonction des genres musicaux préférés',
    'Suggestion amis basée sur des amis communs',
    'Suggestion de groupes basée sur des membres amis',
    'Suggestion de contenu basée sur les publications précédentes',
    'Suggestion de playlists basée sur les habitudes écoute',
    'Suggestion événement/concert basée sur les préférences musicales',
    'Suggestion de groupes participant à des événements'
